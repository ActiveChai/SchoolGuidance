    public void setRegistrationcontent(String registrationcontent) {
        this.registrationcontent = registrationcontent;
    }

    public String getRegitrationtime() {
        return regitrationtime;
    }

    public void setRegitrationtime(String regitrationtime) {
        this.regitrationtime = regitrationtime;
    }

