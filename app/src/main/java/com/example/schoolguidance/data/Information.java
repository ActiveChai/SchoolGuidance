package com.example.schoolguidance.data;

public class Information {
    private int inno;
    private String intitle;
    private String incontent;
    private int adminno;
    private String inmajor;
    private String inreleasetime;

    public int getInno() {
        return inno;
    }

    public void setInno(int inno) {
        this.inno = inno;
    }

    public String getIntitle() {
        return intitle;
    }

    public void setIntitle(String intitle) {
        this.intitle = intitle;
    }

    public String getIncontent() {
        return incontent;
    }

    public void setIncontent(String incontent) {
        this.incontent = incontent;
    }

    public int getAdminno() {
        return adminno;
    }

    public void setAdminno(int adminno) {
        this.adminno = adminno;
    }

    public String getInmajor() {
        return inmajor;
    }

    public void setInmajor(String inmajor) {
        this.inmajor = inmajor;
    }

    public String getInreleasetime() {
        return inreleasetime;
    }

    public void setInreleasetime(String inreleasetime) {
        this.inreleasetime = inreleasetime;
    }
}
